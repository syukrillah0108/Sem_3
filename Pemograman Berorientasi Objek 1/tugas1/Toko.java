package tugas1;

public class Toko {
    private String namaToko;
    private String tipeHandphone;

    public void setNamaToko(String namaToko) {
        this.namaToko = namaToko;
    }

    public void setTipeHandphone(String tipeHandphone) {
        this.tipeHandphone = tipeHandphone;
    }

    public String getNamaToko() {
        return namaToko;
    }

    public String getTipeHandphone() {
        return tipeHandphone;
    }
}